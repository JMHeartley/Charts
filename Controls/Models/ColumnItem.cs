namespace WPFChartControls.Models
{
    /// <summary>
    ///     Represents an item in the column chart with a header and value.
    /// </summary>
    public class ColumnItem
    {
        /// <summary>
        ///     Gets or sets the header of the column item.
        /// </summary>
        public string Header { get; set; }

        /// <summary>
        ///     Gets or sets the value of the column item.
        /// </summary>
        public int Value { get; set; }
    }
}